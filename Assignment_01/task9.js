var a = Number(prompt("Enter number 1 = "));
var b = Number(prompt("Enter number 2 = "));

var diff = a - b;
document.write(`Difference of ${a} and ${b} is ${diff}`);

var product = a * b;
document.write(`<br>Product of ${a} and ${b} is ${product}</br>`);

var quotient = a / b;
document.write(`Quotient of ${a} and ${b} is ${quotient}</br>`);

var modulus = a % b;
document.write(`Modulus of ${a} and ${b} is ${modulus}</br>`);